from typing import List


def sort_by_ext(files: List[str]) -> List[str]:
    l1 = sorted([f for f in files if f.startswith('.') and f.count('.') == 1])
    l2 = sorted([f for f in files if not f.startswith('.') or f.count('.') > 1],
                key=lambda x: x[x.rfind('.'):]
                if '.' in x else x)
    return l1 + l2


